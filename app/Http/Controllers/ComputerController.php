     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Computer $computer)
    {
        if (Gate::allows('update-computer', $computer)) {
            $computer->inspection = !$computer->inspection;
            $computer->save();
            return redirect()->route('computer.show', $computer)->withStatus('Your order was successfull!');
